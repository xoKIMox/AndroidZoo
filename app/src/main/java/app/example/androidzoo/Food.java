package app.example.androidzoo;

public class Food {
    private int food_id;
    private String food_image;
    private String food_name;
    private String food_nutrient;
    private int food_price;
    private String food_spicy;
    private String food_type;

    public int getFood_id() {
        return food_id;
    }

    public void setFood_id(int food_id) {
        this.food_id = food_id;
    }

    public String getFood_image() {
        return food_image;
    }

    public void setFood_image(String food_image) {
        this.food_image = food_image;
    }

    public String getFood_name() {
        return food_name;
    }

    public void setFood_name(String food_name) {
        this.food_name = food_name;
    }

    public String getFood_nutrient() {
        return food_nutrient;
    }

    public void setFood_nutrient(String food_nutrient) {
        this.food_nutrient = food_nutrient;
    }

    public int getFood_price() {
        return food_price;
    }

    public void setFood_price(int food_price) {
        this.food_price = food_price;
    }

    public String getFood_spicy() {
        return food_spicy;
    }

    public void setFood_spicy(String food_spicy) {
        this.food_spicy = food_spicy;
    }

    public String getFood_type() {
        return food_type;
    }

    public void setFood_type(String food_type) {
        this.food_type = food_type;
    }
}